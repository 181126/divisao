public class Operações
{
    public double Adiçao( double n1, double n2)
    {
        return n1 + n2; 
    }
    public double Subtraçao(double n3, double n4)
    {
        return n3 - n4;
    }
    public double Multiplicaçao(double n5, double n6)
    {
        return n5 * n6;
    }
    public double Divisao(double n7, double n8)
    {
        return n7 / n8;
    }
}