class Calculo
{
    public int Soma(int n1, int n2)
    {
        return n1 + n2;
    }
    public int Subtração(int n3, int n4)
    {
        return n3 - n4;
    }
    public int Divisão(int n5, int n6)
    {
        return n5 / n6;
    }
    public int Multiplicação(int n7, int n8)
    {
        return n7 * n8; 
    }

}
